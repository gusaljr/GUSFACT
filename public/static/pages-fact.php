	// Función para obtener la cookie por nombre
	function getCookie(name) {
     console.log(document.cookie);
		let cookieArr = document.cookie.split(";");

		for (let i = 0; i < cookieArr.length; i++) {
			let cookiePair = cookieArr[i].split("=");
			
			if (name == cookiePair[0].trim()) {
				return decodeURIComponent(cookiePair[1]);
			}
		}

		return null;
	}

	// Función para establecer una cookie con nombre, valor y expiración
	function setCookie(name, value, days) {
		let expires = "";
		if (days) {
			let date = new Date();
			date.setTime(date.getTime() + (days * 24 * 60 * 60 * 1000));
			expires = "; expires=" + date.toUTCString();
		}
		document.cookie = name + "=" + (value || "") + expires + "; path=/";
	}

	// Función para añadir un nuevo artículo y regenerar la cookie
	function addArticleToCookie(article) {
		let existingArticles = getCookie('articulos');
		let articlesArray = [];

		if (existingArticles) {
			// Si la cookie existe, la parseamos a un array
			articlesArray = JSON.parse(existingArticles);
		}

		// Añadimos el nuevo artículo al array
		articlesArray.push(article);

		// Guardamos el array actualizado en la cookie
		setCookie('articulos', JSON.stringify(articlesArray), 7);  // Guarda la cookie por 7 días
	}

