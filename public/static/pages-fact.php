	// Función para buscar clientes
	function buscarCliente() {
		// Obtener el valor del input
		var input = document.getElementById('searchInput').value.toLowerCase();

		// Filtrar los clientes y obtener el primer resultado coincidente
		var resultado = clientes.find(function (cliente) {
			var nombre = cliente.Nombre ? cliente.Nombre.toLowerCase() : '';
			var dni = cliente.DNI ? cliente.DNI.toLowerCase() : '';
			var direccion = cliente.dir ? cliente.dir.toLowerCase() : '';
			var telefono = cliente.tlf ? cliente.tlf : '';

			// Comparar si el input aparece en alguna de las propiedades
			return nombre.includes(input) || dni.includes(input) || direccion.includes(input) || telefono.includes(input);
		});

		// Mostrar el resultado
		mostrarResultado(resultado);
	}
